    private void actualizarTabla() {
    DefaultTableModel modelo = (DefaultTableModel) tblEmpleado.getModel();
    modelo.setRowCount(0); // Limpiar la tabla

    NodoEmpleado actual = listaEmpleados.getCabeza();

    while (actual != null) {
        Empleado emp = actual.getEmpleado(); // Sacamos el empleado del nodo

        modelo.addRow(new Object[] {
            emp.getNombre(),
            emp.getID(),
            emp.getCargo(),
            emp.getSalario()
        });

        actual = actual.getSiguiente(); // Avanzamos al siguiente nodo
    }
}
    
    private NodoEmpleado obtenerNodoPorID(String id) 
    {
    NodoEmpleado actual = listaEmpleados.getCabeza(); 

    while (actual != null) {
        if (actual.getEmpleado().getID().equals(id)) {
            return actual;
        }
        actual = actual.getSiguiente();
    }
    return null;
    }



