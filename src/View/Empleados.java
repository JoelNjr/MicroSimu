    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:
        String nombre = txtNombre.getText();
        String cargo = txtCargo.getText();
        String ID = txtID.getText();
        double sueldo = Double.parseDouble(txtSalario.getText());

        Empleado nuevo = new Empleado(nombre, ID, cargo, sueldo);

        DatosEmpresa.listaEmpleados.AgregarEmpleado(nuevo);
        DatosEmpresa.guardarDatos();


        JOptionPane.showMessageDialog(null, "Empleado agregado correctamente.");

        actualizarTabla(); // este método tú lo haces para refrescar la tabla visual
    }//GEN-LAST:event_btnAgregarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String buscarID = JOptionPane.showInputDialog("Ingresa el ID del empleado a buscar:");
        Empleado emp = listaEmpleados.buscar(buscarID);

        if (emp != null) {
            txtNombre.setText(emp.getNombre());
            txtID.setText(emp.getID());
            txtCargo.setText(emp.getCargo());
            txtSalario.setText(String.valueOf(emp.getSalario()));
            JOptionPane.showMessageDialog(null, "Empleado encontrado.");
        } else {
            JOptionPane.showMessageDialog(null, "Empleado no encontrado.");
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        int fila = tblEmpleado.getSelectedRow(); // Obtenemos la fila seleccionada

        if (fila >= 0) {
            // Obtenemos el ID (columna 1 en la tabla, ya que empieza en 0)
            String id = (String) tblEmpleado.getValueAt(fila, 1);

            boolean eliminado = listaEmpleados.eliminarEmpleado(id); // Usamos tu método
            DatosEmpresa.guardarDatos();


            if (eliminado) {
                actualizarTabla(); // Refrescamos la tabla
                JOptionPane.showMessageDialog(null, "Empleado eliminado correctamente.");
            } else {
                JOptionPane.showMessageDialog(null, "No se encontró el empleado con ese ID.");
            }
        } else {
            JOptionPane.showMessageDialog(null, "Selecciona una fila para eliminar.");
        }
    
    }//GEN-LAST:event_btnEliminarActionPerformed

