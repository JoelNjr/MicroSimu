    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
          String id = txtID.getText().trim();

        NodoEmpleado actual = obtenerNodoPorID(id);

        if (actual != null) {
            Empleado emp = actual.getEmpleado();

            // Actualizamos sus datos con los valores del formulario
            emp.setNombre(txtNombre.getText());
            emp.setCargo(txtCargo.getText());
            emp.setSalario(Double.parseDouble(txtSalario.getText()));
            
            DatosEmpresa.guardarDatos();
            actualizarTabla();
            JOptionPane.showMessageDialog(null, "Empleado actualizado correctamente.");
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró el empleado a actualizar.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

