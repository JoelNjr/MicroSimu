    private void btnRegistrarTransaccionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarTransaccionActionPerformed
        // TODO add your handling code here:
        double total1;
        
         try {
        String tipo = cbTipoTransaccion.getSelectedItem().toString();
        String nombreProducto = cbProductoTransaccion.getSelectedItem().toString();
        String nombreEmpleado = cbEmpleadoTransaccion.getSelectedItem().toString();
        int cantidad = Integer.parseInt(txtCantidadTransaccion.getText());

        // Buscar objeto Producto
        Producto producto = DatosEmpresa.listaProductos.buscarpornombre(nombreProducto);
        if (producto == null) {
            JOptionPane.showMessageDialog(this, "Producto no encontrado.");
            return;
        }

        // Validar stock si es venta
        if (tipo.equals("Venta") && producto.getStock() < cantidad) {
            JOptionPane.showMessageDialog(this, "No hay suficiente stock para esta venta.");
            return;
        }

        // Actualizar stock según tipo
        if (tipo.equals("Venta")) {
            producto.setStock(producto.getStock() - cantidad);
        } else {
            producto.setStock(producto.getStock() + cantidad);
        }

        // Buscar objeto Empleado
        Empleado empleado = DatosEmpresa.listaEmpleados.buscarpornombre(nombreEmpleado);
        if (empleado == null) {
            JOptionPane.showMessageDialog(this, "Empleado no encontrado.");
            return;
        }
        
        if(cbTipoTransaccion.getSelectedItem().toString().equalsIgnoreCase("compra"))
        {
            total1 = producto.getPreciocompra() * cantidad;
        }
        else
        {
            total1 = producto.getPrecioventa() * cantidad;
        }
        
        double total = total1;

        
        Transaccion nueva = new Transaccion(tipo, nombreProducto, cantidad, nombreEmpleado, total);

        DatosEmpresa.colaTransacciones.encolar(nueva);
        DatosEmpresa.guardarDatos();
        JOptionPane.showMessageDialog(this, "Transacción registrada correctamente.");
        new Transacciones(empresa).setVisible(true);
        this.dispose();

        
        
        limpiarCampos();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_btnRegistrarTransaccionActionPerformed

