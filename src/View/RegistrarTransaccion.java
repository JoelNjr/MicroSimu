    private void cargarCombos() {
    cbProductoTransaccion.removeAllItems();
    cbEmpleadoTransaccion.removeAllItems();

    NodoProducto p = DatosEmpresa.listaProductos.getCabeza();
    while (p != null) {
        cbProductoTransaccion.addItem(p.getProducto().getNombre());
        p = p.getSiguiente();
    }

    NodoEmpleado e = DatosEmpresa.listaEmpleados.getCabeza();
    while (e != null) {
        cbEmpleadoTransaccion.addItem(e.getEmpleado().getNombre());
        e = e.getSiguiente();
    }
   }
    
